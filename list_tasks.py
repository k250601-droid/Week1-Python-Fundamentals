numbers = [5, 2, 9, 1, 5, 6]

# Max & Min
print("Max:", max(numbers))
print("Min:", min(numbers))

# Remove duplicates
unique_numbers = list(set(numbers))
print("Without duplicates:", unique_numbers)

# Sort ascending
print("Ascending:", sorted(numbers))

# Sort descending
print("Descending:", sorted(numbers, reverse=True))